int	ft_atoi(const char *str)
{
	int	np;
	int	val;

	np = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
	{
		str++;
	}
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			np = -np;
		str++;
	}
	val = 0;
	while (*str >= '0' && *str <= '9')
	{
		val = (val * 10) + ((*str - '0') * np);
		str++;
	}
	return (val);
}
