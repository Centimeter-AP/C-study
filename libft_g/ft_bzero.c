#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;
	size_t			i;

	i = 0;
	tmp = (unsigned char *)s;
	if (n == 0)
		return ;
	while (n--)
	{
		tmp[i] = 0;
		i++;
	}
}
